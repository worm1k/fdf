#include "fdf.h"

void	print_struct(t_data *data)
{
	int	i;
	int	j;

	i = 0;
	while (i < data->rows)
	{
		j = 0;
		while (j < data->cols)
		{
			printf("%f", data->point[i][j].z);
			if (data->point[i][j].color != 0xFFFFFF)
				printf(",%x", data->point[i][j].color);
			j++;
			printf("  ");
		}
		printf("\n");
		i++;
	}
}